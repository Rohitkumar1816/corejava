package introduction;


public class variable {
    public static void main(String[] args) {


        //integer type
        byte age = 12;   //by default value is 0
        short year = 2025;//by default value is 0
        int salary = 203333;//by default value is 0
        long req = 12334565L;//by default value is 0
        //floating type//
        float pay = 240.033f;//by default value is 00.00f
        double bigpay = 2334343244d;//by default value is 000000.00d

//    boolean type
        boolean flag = true; //by default value is false

        //char type

        char GRADE = 'a';  //by default value is  u00`
        System.out.println("age"+age);
        System.out.println("year"+year);
        System.out.println("salary"+salary);
        System.out.println("req"+req);
        System.out.println("Pay"+pay);
        System.out.println("bigpay"+bigpay);
        System.out.println("flag"+flag);
        System.out.println("grade"+GRADE);


    }
}